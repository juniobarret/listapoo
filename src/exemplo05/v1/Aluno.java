package exemplo05.v1;

public class Aluno {
    private String nome;
    private String cpf;
    private String telefone;
    private String email;
    private String curso;

    public Aluno(String nome, String cpf, String telefone, String email, String curso) {
        this.nome = nome;
        this.cpf = cpf;
        this.telefone = telefone;
        this.email = email;
        this.curso = curso;
    }
}
